#!/usr/bin/env ruby
# frozen_string_literal: true

# Provider health monitor.
#
# Fetches the live /v2/providers feed and flags any provider whose
# `publishes_missed` exceeds a cadence-specific threshold, then upserts a single
# rolling GitHub issue (label: provider-health). The point is to surface silent
# freezes — a provider whose feed breaks (TLS chain, source change, geo-block)
# fails every scheduled backfill but logs nothing a human sees.
#
# `publishes_missed` is already cadence-aware in its unit (missed fire-days for
# daily, missed week/month buckets for weekly/monthly), computed server-side, so
# this script only thresholds it. Thresholds sit above normal lag: daily absorbs
# holiday clusters and T+1 publishing; monthly absorbs in-arrears archives.
#
# Run with DRY_RUN=1 to print the decision and body without touching issues.

require "English"
require "json"
require "net/http"
require "uri"
require "date"

API = ENV.fetch("API", "https://api.frankfurter.dev/v2/providers")
REPO = ENV.fetch("REPO", "lineofflight/frankfurter")
LABEL = "provider-health"
MARKER = "<!-- provider-health -->"

# Flag only well past normal lag. See bin/provider_health.rb header and the
# calibration in the PR that introduced this for why these values.
THRESHOLDS = { "daily" => 8, "weekly" => 2, "monthly" => 2 }.freeze

def fetch_providers
  attempts = 0
  begin
    JSON.parse(Net::HTTP.get(URI(API)))
  rescue OpenSSL::SSL::SSLError, SocketError, Net::OpenTimeout, Net::ReadTimeout, Errno::ECONNRESET
    attempts += 1
    raise if attempts > 3

    sleep(2**attempts)
    retry
  end
end

def flagged(providers)
  providers
    .select do |p|
      threshold = THRESHOLDS[p["publish_cadence"]]
      threshold && (p["publishes_missed"] || 0) >= threshold
    end
    .sort_by { |p| -(p["publishes_missed"] || 0) }
end

def render_body(flag, today)
  set = flag.map { |p| p["key"] }.sort.join(",")
  rows = flag.map do |p|
    "| #{p["key"]} | #{p["publish_cadence"]} | #{p["end_date"]} | #{p["publishes_missed"]} |"
  end

  <<~MARKDOWN
    #{MARKER}
    <!-- flagged: #{set} -->
    ### Stale providers

    Missed more scheduled publishes than normal lag allows — likely a broken feed, not a holiday gap.

    | Provider | Cadence | Last rate | Missed |
    |----------|---------|-----------|--------|
    #{rows.join("\n")}

    _Checked #{today} UTC. Auto-generated by `.github/workflows/provider-health.yml`; closes when all clear._
  MARKDOWN
end

def gh(*args)
  output = IO.popen(["gh", *args], &:read)
  raise "gh #{args.join(" ")} failed" unless $CHILD_STATUS.success?

  output
end

def open_issue
  json = gh(
    "issue",
    "list",
    "--repo",
    REPO,
    "--state",
    "open",
    "--label",
    LABEL,
    "--json",
    "number,body",
    "--limit",
    "1",
  )
  JSON.parse(json).first
end

def previously_flagged(body)
  match = body.to_s.match(/<!-- flagged: (.*?) -->/)
  match ? match[1].split(",").reject(&:empty?) : []
end

def main
  flag = flagged(fetch_providers)
  today = Date.today.to_s

  if ENV["DRY_RUN"]
    warn("DRY_RUN — flagged: #{flag.map { |p| p["key"] }.inspect}")
    warn(flag.empty? ? "(would close any open issue / no-op)" : render_body(flag, today))
    return
  end

  issue = open_issue

  if flag.empty?
    return unless issue

    gh("issue", "comment", issue["number"].to_s, "--repo", REPO, "--body", "All providers healthy again — closing.")
    gh("issue", "close", issue["number"].to_s, "--repo", REPO)
    return
  end

  body = render_body(flag, today)

  if issue
    newly = flag.map { |p| p["key"] } - previously_flagged(issue["body"])
    gh("issue", "edit", issue["number"].to_s, "--repo", REPO, "--body", body)
    gh("issue", "comment", issue["number"].to_s, "--repo", REPO, "--body", "Newly stale: #{newly.sort.join(", ")}.") if newly.any?
  else
    gh("issue", "create", "--repo", REPO, "--label", LABEL, "--title", "Stale providers", "--body", body)
  end
end

main if $PROGRAM_NAME == __FILE__
